#!/usr/bin/env python3
# -*- coding: utf-8 -*-

if __name__ == "__main__":
    # Создание словаря с классами и количеством учащихся
    school = {"1а": 25, "1б": 24, "2б": 23, "6а": 27, "7в": 26}

    print("Исходный словарь:")
    print(school)

    # а) В одном из классов изменилось количество учащихся
    school["1а"] = 28
    print("\nПосле изменения количества в 1а классе:")
    print(school)

    # б) В школе появился новый класс
    school["3а"] = 22
    print("\nПосле добавления нового класса 3а:")
    print(school)

    # с) В школе был расформирован класс
    del school["2б"]
    print("\nПосле удаления класса 2б:")
    print(school)

    # Вычисление общего количества учащихся
    total_students = sum(school.values())
    print(f"\nОбщее количество учащихся в школе: {total_students}")
